package com.example.petmily.model;

public class Post_half {
    String place;
    int post;

    public Post_half(String place, int post) {
        this.place = place;
        this.post = post;
    }

    public String getPlace() {
        return place;
    }

    public void setPlace(String place) {
        this.place = place;
    }

    public int getPost() {
        return post;
    }

    public void setPost(int post) {
        this.post = post;
    }
}
